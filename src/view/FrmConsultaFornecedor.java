    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        jTextFieldCodigoClicado.setText("");
        jTextFieldMarcaClicada.setText("");
        
        if(jComboBoxTipoFiltro.getSelectedItem().equals("Nome")){
            if(jTextFieldCampoFiltro.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Informe um fornecedor");
            }else{
                preencherTabelaMarca("select *from tblFornecedor where nomeFormecedor = '"+jTextFieldCampoFiltro.getText()+"'");   
            }           
        }else if(jComboBoxTipoFiltro.getSelectedItem().equals("Contem")){
            if(jTextFieldCampoFiltro.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Informe um fornecedor");
            }else{
                preencherTabelaMarca("select *from tblfornecedor where nomeFormecedor like '%"+jTextFieldCampoFiltro.getText()+"'"); 
            }             
        }else if (jComboBoxTipoFiltro.getSelectedItem().equals("Código")){
            if(jTextFieldCampoFiltro.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Informe o código de um fornecedor");
            }else{
                preencherTabelaMarca("select *from tblfornecedor where idFornecedor = '"+jTextFieldCampoFiltro.getText()+"'"); 
            }            
        }else if(jComboBoxTipoFiltro.getSelectedItem().equals("Tudo")){
           preencherTabelaMarca("select * from tblfornecedor"); 
        }
        
        jButtonAdd.setEnabled(!true);
        
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

