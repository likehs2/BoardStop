    private void jTableMarcaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMarcaMouseClicked
        int linha_selecionada = jTableMarca.getSelectedRow();
        setIdMarca(jTableMarca.getValueAt(linha_selecionada, 0).toString());
        setNomeMarca(jTableMarca.getValueAt(linha_selecionada, 1).toString());
                
        String UnidadeID = "" + jTableMarca.getValueAt(jTableMarca.getSelectedRow(), 0);
        jTextFieldCodigoClicado.setText(UnidadeID);
        
        String SiglaUnidade = "" + jTableMarca.getValueAt(jTableMarca.getSelectedRow(), 1);
        jTextFieldMarcaClicada.setText(SiglaUnidade);
        
        jButtonAdd.setEnabled(true);
    }//GEN-LAST:event_jTableMarcaMouseClicked

    public void preencherTabelaMarca(String sql){
        ArrayList dados = new ArrayList();
        
        String[] Colunas = new String[]{"ID","Fornecedor"};
        conn.conexao();
        conn.executaSQL(sql);
        try{
            conn.rs.first();
            do{
                dados.add(new Object[]{ conn.rs.getString("idFornecedor") ,  conn.rs.getString("nomeFormecedor")});
                
            }while (conn.rs.next());
        }catch (SQLException ex){
            //JOptionPane.showMessageDialog(null, "Erro ao preencher o ArrayList!\nERRO:" + ex);
         //catch(NumberFormatException erro){
                JOptionPane.showMessageDialog(null, "Fornecedor não encontrado");
        }
        
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTableMarca.setModel(modelo);
        jTableMarca.getColumnModel().getColumn(0).setPreferredWidth(40);
        jTableMarca.getColumnModel().getColumn(0).setResizable(false);
        jTableMarca.getColumnModel().getColumn(1).setPreferredWidth(300);
        jTableMarca.getColumnModel().getColumn(1).setResizable(false);
        jTableMarca.getTableHeader().setReorderingAllowed(false);
        jTableMarca.setAutoResizeMode(jTableMarca.AUTO_RESIZE_OFF);
        jTableMarca.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
