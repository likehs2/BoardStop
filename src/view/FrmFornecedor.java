    public void buscarUltimaInsercao() {
        try {
            modFornecedor.setNomeFornecedor(jTextFieldNomeFornecedor.getText());
            modFornecedor = controlFornecedor.UltimaInsercao(modFornecedor);

            jTextFieldCodigo.setText(String.valueOf(modFornecedor.getIdFornecedor()));

        } catch (NumberFormatException erro) {
            JOptionPane.showMessageDialog(null, "Digite um cÃ³digo vÃ¡lido");
        }
    }

